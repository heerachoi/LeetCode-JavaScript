// 709. To Lower Case

// Given a string s, return the string after replacing every uppercase letter with the same lowercase letter.

// Example 1:
// Input: s = "Hello"
// Output: "hello"

// Example 2:
// Input: s = "here"
// Output: "here"

/**
 * @param {string} str
 * @return {string}
 */
 var toLowerCase = function(str) {
    return str.toLowerCase();
   
};